package Operators;

public class Arithmetic_Operators {
	
	public static void main (String[] args)
	{
		double A = 100;
		double B = 150;
		
		System.out.println(A+B);
		System.out.println(A*B);
		System.out.println(A-B);
		System.out.println(A/B);
		System.out.println(A%B);			
	}

}
