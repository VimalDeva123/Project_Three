package Loops;

public class While_Loop {

	public static void main(String[] args) {
		
		
		int L = 11 ;
		while (L<=31)
		{
			System.out.println(L);
		      L = L +2;	
		}
		
		
		int M = 60;
		
		while (M>=0)
		{
			System.out.println(M);
			M = M - 6;
		}
		
		
		
		
		

	}

}
