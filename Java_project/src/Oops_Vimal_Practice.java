
public class Oops_Vimal_Practice 
{
	public static void main (String[]args)
	{
		System.out.println(Oops_Vimal_Practice.Max(1500, 200));;	
		System.out.println(Oops_Vimal_Practice.Max(15.0, 5));
	}
	
	
	static int Max (int A , int B)
	{
		return A>B ? A:B ;
	}
	
	static double Max (double A ,double B)
	{
	return A<B ? A:B;	
	}
	
	

}
