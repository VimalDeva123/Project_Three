package Daily_Tasks;

public class While_Loop {
	
	public static void main(String[]args)
	{
		
		// Task Print even Numbers Only using Java While loop ;
		
           int Count = 1;
            while (Count <= 20)
            {
            	if (Count % 2 == 0 )
            	{
            		System.out.println(Count);
            	}
            	
            	Count ++ ;
            	
            }
			

		
		
	}

}
